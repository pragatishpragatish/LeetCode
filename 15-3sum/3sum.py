class Solution:
    def threeSum(self, nums: List[int]) -> List[List[int]]:
        nums.sort()
        res = []

        for i, x in enumerate(nums):
            if i>0 and x == nums[i-1]:
                continue
            l, r = i + 1, len(nums) - 1

            while l < r:
                num = nums[l] + nums[r] + x

                if num > 0:
                    r -= 1
                elif num < 0:
                    l += 1
                else:
                    res.append([x, nums[l], nums[r]])
                    l += 1
                    while l<r and nums[l] == nums[l-1]:
                        l += 1

        return res